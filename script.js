console.clear();

// Arrays
const myArray = [];

// Add elements to an array
myArray[0] = 'VedaGna';
myArray[1] = 5;
myArray[2] = true;

myArray.push('school'); //The push() method adds one or more elements to the end of an array and returns the new length of the array.
myArray.pop('school'); //The pop() method removes the last element from an array and returns that element. This method changes the length of the array.

console.log(myArray); // [ 'VedaGna', 5, true ]