console.clear();

// Conditionals: Switch Statements

switch (Math.floor(Math.random() * 5 + 1)) {
    case 1:
        console.log('1');
        break;

    case 2:
        console.log('2');
        break;

    case 3:
        console.log('3');
        break;

    default:
        console.log('No Match');
}